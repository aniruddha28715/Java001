public class IncreDecreOperator {
    public static void main(String[] args) {
        int a = 7;
        
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);

        int x = ++a*8;
        System.out.println(x);

        char g = 'b';
        System.out.println(g++);


}
}


